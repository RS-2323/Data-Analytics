"""Hands On 1
# Print all the numbers from 1 to 10 using condition in while loop

"""
number= 1
while(number<=10):
    print(number)
    number+=1
    
"""
Hands On 2
"""
#  Print all the numbers from 1 to 10 using while True loop
number= 1
while(True):
    if(number<=10):
        print(number)
    else:
        break
    number+=1
    
